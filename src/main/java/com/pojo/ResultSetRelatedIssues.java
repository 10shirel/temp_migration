public class ResultSetRelatedIssues {
    String relationType;
    String  primaryIssueId;
    String  secondaryIssueId;

    public String getRelationType() {
        return relationType;
    }

    public void setRelationType(String relationType) {
        this.relationType = relationType;
    }

    public String getPrimaryIssueId() {
        return primaryIssueId;
    }

    public void setPrimaryIssueId(String primaryIssueId) {
        this.primaryIssueId = primaryIssueId;
    }

    public String getSecondaryIssueId() {
        return secondaryIssueId;
    }

    public void setSecondaryIssueId(String secondaryIssueId) {
        this.secondaryIssueId = secondaryIssueId;
    }


}
